#!/usr/bin/env node
/**
 * Writes src/lib/coachRegistry.generated.ts with require() for each MP3 in packages/coach-audio.
 * Run from repo root: pnpm --filter @boxing-coach/mobile run generate:coach-registry
 */
import { readdirSync, writeFileSync, mkdirSync, existsSync } from 'fs';
import { dirname, join } from 'path';
import { fileURLToPath } from 'url';

const __dirname = dirname(fileURLToPath(import.meta.url));
const MOBILE_ROOT = join(__dirname, '..');
const REPO_ROOT = join(MOBILE_ROOT, '..', '..');
const COACH_DIR = join(REPO_ROOT, 'packages', 'coach-audio');
const OUT = join(MOBILE_ROOT, 'src', 'lib', 'coachRegistry.generated.ts');

function main() {
  let ids = [];
  if (existsSync(COACH_DIR)) {
    ids = readdirSync(COACH_DIR)
      .filter((f) => f.endsWith('.mp3'))
      .map((f) => f.replace(/\.mp3$/i, ''))
      .sort();
  }

  const lines = ids.map(
    (id) => `  '${id}': require('../../assets/audio/coach/${id}.mp3'),`,
  );

  const body = `/**
 * Generated by scripts/generate-coach-registry.mjs — do not edit by hand.
 * Re-run: pnpm --filter @boxing-coach/mobile run generate:coach-registry
 */
export const coachRegistry: Record<string, number> = {
${lines.join('\n')}
};
`;

  mkdirSync(dirname(OUT), { recursive: true });
  writeFileSync(OUT, body, 'utf-8');
  console.log(`Wrote ${OUT} (${ids.length} clips)`);
}

main();
